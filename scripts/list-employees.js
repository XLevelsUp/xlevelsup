/**
 * List all employees with their default login credentials
 */

const dotenv = require('dotenv');
const path = require('path');

// Load environment variables
dotenv.config({ path: path.resolve(process.cwd(), '.env.local') });

const { createClient } = require('@supabase/supabase-js');

const supabaseUrl = process.env.NEXT_PUBLIC_SUPABASE_URL;
const supabaseKey = process.env.SUPABASE_SERVICE_ROLE_KEY;

const supabase = createClient(supabaseUrl, supabaseKey);

async function listEmployees() {
  try {
    console.log('\n🔍 Fetching employee accounts...\n');

    const { data: employees, error } = await supabase
      .from('employees')
      .select(
        'id, employee_id, name, email, department, role, status, account_status',
      )
      .order('employee_id');

    if (error) {
      console.error('❌ Error fetching employees:', error);
      process.exit(1);
    }

    if (!employees || employees.length === 0) {
      console.log('⚠️  No employees found in the database');
      process.exit(0);
    }

    console.log('📋 EMPLOYEE ACCOUNTS:\n');
    console.log('='.repeat(100));

    employees.forEach((emp, index) => {
      console.log(`\n${index + 1}. ${emp.name}`);
      console.log(`   Employee ID:      ${emp.employee_id}`);
      console.log(`   Email:            ${emp.email}`);
      console.log(`   Department:       ${emp.department}`);
      console.log(`   Role:             ${emp.role}`);
      console.log(`   Status:           ${emp.status}`);
      console.log(`   Account Status:   ${emp.account_status || 'active'}`);
      console.log(`   Default Password: Welcome@${emp.employee_id}`);
      console.log(`   Login URL:        http://localhost:3000/employee/login`);
    });

    console.log('\n' + '='.repeat(100));
    console.log(`\n📊 Total Employees: ${employees.length}`);
    console.log('\n⚠️  SECURITY NOTES:');
    console.log('   • These are DEFAULT passwords generated by the system');
    console.log('   • Password format: Welcome@[EmployeeID]');
    console.log('   • All employees MUST change their password on first login');
    console.log('   • Use these credentials ONLY for testing/development');
    console.log('   • Never share passwords in production environments');
    console.log('\n💡 ADMIN LOGIN:');
    console.log('   • URL:      http://localhost:3000/erp/login');
    console.log('   • Email:    admin@xlevelsup.com');
    console.log('   • Password: XLU-admin-9x8f-2a3b');
    console.log('\n');
  } catch (error) {
    console.error('❌ Error:', error.message);
    process.exit(1);
  }
}

listEmployees();
